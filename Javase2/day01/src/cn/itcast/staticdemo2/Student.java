package cn.itcast.staticdemo2;

/**
 * 静态方法
 */
public class Student {


    /**
     * 静态方法
     */
    public static void printHelloWorld() {
        System.out.println("静态方法:" + "HelloWorld");
    }

    /**
     * 成员方法
     */
    public void printPass() {
        System.out.println("实例方法:" + "pass");
    }

}
