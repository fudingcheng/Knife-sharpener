package cn.itcast.loop;

/**
 * do...while:打印3遍HelloWorld
 */
public class DoWhileDemo {
    public static void main(String[] args) {
        // 初始化语句
        int i = 0;

        //do...while
        do {
            //打印HelloWorld
            System.out.println("HelloWorld");
            // 迭代语句,操作变量
            i++;
        } while (false);
    }
}
